function [lhs, rhs] = dynamic_resid(y, x, params, steady_state)
T = NaN(2, 1);
lhs = NaN(10, 1);
rhs = NaN(10, 1);
T(1) = params(1)/(1/params(2)-(1-params(3)));
T(2) = params(3)*T(1);
lhs(1) = y(11);
rhs(1) = y(12)*(1-T(2))+y(14)*T(2);
lhs(2) = 1/params(5)*y(26);
rhs(2) = y(22)-y(12);
lhs(3) = y(12)*params(5);
rhs(3) = y(11)-y(13);
lhs(4) = y(16);
rhs(4) = (y(11)-y(5))*params(1)*1/T(1);
lhs(5) = y(15);
rhs(5) = (1-params(3))*y(5)+params(3)*y(14);
lhs(6) = y(11);
rhs(6) = y(17)+params(1)*y(5)+y(13)*(1-params(1));
lhs(7) = y(17);
rhs(7) = params(4)*y(7)+x(1);
lhs(8) = y(18);
rhs(8) = y(11)-y(1)+params(7);
lhs(9) = y(19);
rhs(9) = y(12)-y(2);
lhs(10) = y(20);
rhs(10) = y(14)-y(4);
end
