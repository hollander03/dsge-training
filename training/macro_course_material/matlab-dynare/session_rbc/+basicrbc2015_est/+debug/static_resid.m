function [lhs, rhs] = static_resid(y, x, params)
T = NaN(2, 1);
lhs = NaN(12, 1);
rhs = NaN(12, 1);
T(1) = params(1)/(1/params(2)-(1-params(3)));
T(2) = params(3)*T(1);
lhs(1) = y(1);
rhs(1) = y(11)+(1-T(2))*y(2)+T(2)*y(4);
lhs(2) = 1/params(7)*y(6);
rhs(2) = y(12);
lhs(3) = y(2)*params(7);
rhs(3) = y(1)-y(3);
lhs(4) = y(6);
rhs(4) = (y(1)-y(5))*params(1)*1/T(1);
lhs(5) = y(5);
rhs(5) = (1-params(3))*y(5)+params(3)*y(4);
lhs(6) = y(1);
rhs(6) = y(7)+params(1)*y(5)+y(3)*(1-params(1));
lhs(7) = y(7);
rhs(7) = y(7)*params(4)+x(1);
lhs(8) = y(11);
rhs(8) = y(11)*params(5)+x(2);
lhs(9) = y(12);
rhs(9) = y(12)*params(6)+x(3);
lhs(10) = y(8);
rhs(10) = y(1);
lhs(11) = y(9);
rhs(11) = params(10);
lhs(12) = y(10);
rhs(12) = params(11);
end
